CREATE TABLE IF NOT EXISTS player
(
    ID          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PSEUDO      VARCHAR(256) NOT NULL,
    OWNER       VARCHAR(256) NOT NULL,
    EXPERIENCE  BIGINT NOT NULL DEFAULT 0,
    LEVEL       INT NOT NULL DEFAULT 1,
    GOLD        BIGINT NOT NULL DEFAULT 0,
    HP          DOUBLE NOT NULL DEFAULT 100,
    ATK         DOUBLE NOT NULL DEFAULT 10,
    DEFENSE     DOUBLE NOT NULL DEFAULT 5,
    ATK_SPD     DOUBLE NOT NULL DEFAULT 1.0,
    CRIT_RATE   DOUBLE NOT NULL DEFAULT 5.0,
    CRIT_DMG    DOUBLE NOT NULL DEFAULT 150.0
);